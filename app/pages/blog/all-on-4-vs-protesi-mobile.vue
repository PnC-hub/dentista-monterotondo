            <p>
              La protesi mobile totale, comunemente chiamata <strong>dentiera</strong>, e un dispositivo
              rimovibile che sostituisce tutti i denti di un'arcata. Si appoggia sulla gengiva e viene
              tenuta in sede dalla suzione e, in molti casi, dall'uso di <strong>adesivi</strong> (pasta adesiva).
