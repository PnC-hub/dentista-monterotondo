            <h2>Come Funziona il Processo di Applicazione</h2>
            <p>
              Nel nostro studio di <strong>Monterotondo</strong>, il percorso per le faccette estetiche
              segue un protocollo in 4 fasi che garantisce risultati prevedibili e personalizzati.
            </p>

            <h3>Fase 1: Smile Design Digitale</h3>
            <p>
              Il primo passo e l'analisi estetica del viso e del sorriso. Attraverso fotografie cliniche
              e un software dedicato, il dentista progetta il <strong>sorriso ideale</strong> tenendo conto
              delle proporzioni del viso, della linea del sorriso, del colore della pelle e delle
              aspettative del paziente. Il risultato viene mostrato al paziente prima di qualsiasi intervento.
            </p>

            <h3>Fase 2: Mock-up e Prova Estetica</h3>
            <p>
              Sulla base del progetto digitale, viene realizzato un <strong>mock-up provvisorio</strong>
              in resina direttamente sui denti del paziente. Questo permette di "provare" il nuovo
              sorriso nella vita reale, valutandone forma, colore e proporzioni prima della
              preparazione definitiva.
            </p>

            <h3>Fase 3: Preparazione e Impronta Digitale</h3>
            <p>
              Una volta approvato il mock-up, si procede con la <strong>preparazione minimamente invasiva</strong>
              dei denti. Viene rimosso uno strato di smalto compreso tra 0,3 e 0,5 mm (meno di mezzo millimetro).
              L'impronta viene presa con uno <strong>scanner intraorale 3D</strong>, eliminando completamente
              la necessita delle paste da impronta tradizionali.
            </p>

            <h3>Fase 4: Cementazione Definitiva</h3>
            <p>
              Le faccette, realizzate dal laboratorio odontotecnico, vengono provate e poi cementate
              con una tecnica <strong>adesiva</strong> che garantisce una tenuta eccezionale.
              Il risultato e immediato: il paziente esce dallo studio con il nuovo sorriso.
            </p>

