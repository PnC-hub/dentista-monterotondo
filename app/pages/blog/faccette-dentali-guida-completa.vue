            <h2>Faccette Dentali: Domande Frequenti</h2>
            <ul class="feature-list">
              <li><strong>Fanno male?</strong> — La procedura viene eseguita in anestesia locale. Nessun dolore durante il trattamento e minimo fastidio nei giorni successivi.</li>
              <li><strong>Si vedono che sono faccette?</strong> — Con i materiali e le tecniche attuali, le faccette sono indistinguibili dai denti naturali.</li>
              <li><strong>Posso mordere normalmente?</strong> — Si, con le normali precauzioni. Le faccette hanno una resistenza paragonabile allo smalto naturale.</li>
              <li><strong>Sono reversibili?</strong> — Le faccette in ceramica richiedono una preparazione del dente e non sono reversibili. Le faccette in composito diretto possono essere rimosse.</li>
              <li><strong>Quante sedute servono?</strong> — Per le faccette in ceramica, generalmente 2-3 sedute distribuite in 2-3 settimane. Per il composito diretto, una sola seduta.</li>
            </ul>

