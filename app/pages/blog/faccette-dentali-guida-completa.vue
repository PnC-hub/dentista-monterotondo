            <h2>Cosa Sono le Faccette Dentali?</h2>
            <p>
              Le <strong>faccette dentali</strong> (o veneers) sono sottilissime lamine in ceramica o materiale
              composito che vengono cementate sulla superficie anteriore dei denti per correggerne
              <strong>colore, forma, dimensione e allineamento</strong>. Con uno spessore compreso tra
              0,3 e 0,7 mm, le faccette rappresentano una delle soluzioni estetiche meno invasive
              e piu efficaci dell'odontoiatria moderna.
            </p>
            <p>
              Nel nostro studio di <strong>Monterotondo</strong>, il Dott. Piernatale Civero utilizza
              la tecnica dello <strong>Smile Design digitale</strong> per progettare il sorriso ideale
              prima ancora di iniziare il trattamento: il paziente puo vedere il risultato finale
              sul monitor e provare un mock-up direttamente in bocca.
            </p>

            <h2>Tipologie di Faccette Dentali</h2>
            <p>
              Esistono diverse tipologie di faccette, ciascuna con caratteristiche specifiche.
              La scelta dipende dal caso clinico, dalle aspettative estetiche e dal budget del paziente.
            </p>

            <h3>Faccette in Ceramica Integrale (Feldspato)</h3>
            <p>
              Le faccette in ceramica feldspacica sono il <strong>gold standard</strong> dell'estetica dentale.
              Offrono una traslucenza naturale che imita perfettamente lo smalto dentale. La ceramica
              e altamente resistente alle macchie (caffe, te, vino rosso) e mantiene il suo colore
              nel tempo. La durata media e di <strong>15-20 anni</strong> con una corretta manutenzione.
            </p>

            <h3>Faccette in Disilicato di Litio (e.max)</h3>
            <p>
              Il disilicato di litio (comunemente noto come <strong>e.max</strong>) combina estetica
              e resistenza meccanica. Queste faccette hanno una trasparenza eccezionale che le rende
              ideali per i <strong>denti anteriori</strong>, dove la luce naturale deve poter filtrare
              attraverso il dente. Sono leggermente piu resistenti della ceramica feldspacica
              e hanno una durata comparabile di 15-20 anni.
            </p>

            <h3>Faccette in Composito</h3>
            <p>
              Le faccette in composito rappresentano una soluzione <strong>reversibile e piu accessibile</strong>.
              Vengono realizzate direttamente in bocca (tecnica diretta) o in laboratorio (tecnica indiretta).
              La durata media e di <strong>5-7 anni</strong>. Sono ideali per piccole correzioni estetiche
              o per pazienti giovani che desiderano testare il risultato prima di passare alla ceramica.
            </p>

